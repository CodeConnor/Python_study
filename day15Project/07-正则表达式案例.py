# ---------------------------------------------------------eg.1
# 查找字符串中是否包含数字"8"
# 1、导入模块
import re
# 2、匹配结果
str1 = '13086062869'
result = re.findall('8', str1)
print(result)

# ---------------------------------------------------------eg.2
# 查找字符串中是否包含任意数字
str2 = 'abcd6efgh'
result = re.findall('\d', str2)  # \d 代表数字
print(result)

# ---------------------------------------------------------eg.3
# 查找字符串中所有非数字字符
str3 = 'sbcd234aa@#$%^&*'
result = re.findall('\D', str3)  # \D 代表非数字
print(result)